# 5- Membership Operators  (in , not in)
 # it is used to check data is present in  in data type or not 
 # in 

list=[1,2,3,4,5] #list data type
sub=5
print(sub in list ) #true

list=[1,2,3,4,5] #list data type
sub=6
print(sub in list ) #false

#not in
l=[1,2,3,4,5] #list data type

sub=5
print(sub not in l) #false 

l=[1,2,3,4,5] #list data type
sub=6
print(sub not in l) #true
