# what is loops 
# loops are used to execute a block of code repeatedly for a specified number of times
# there are two types of loops in python
# 1. for loop
# 2. while loop
# for loop is used to iterate over a sequence (such as a list, tuple, dictionary,
#  set, etc.) and execute a block of code for each item in the sequence

# while loop is used to execute a block of code as long as a certain condition is true
# it will continue to execute the block of code until the condition becomes false

# we can use the break and continue statements to control the flow of the loop
# we can use the range() function to generate a sequence of numbers for the loop to iterate over
# we can use the enumerate() function to get the index and value of each item in the sequenc
# we can use the zip() function to iterate over two or more sequences in parallel
# we can use the any() and all() functions to check if any or all items in a
# sequence meet a certain condition
# we can use the next() function to get the next item from a sequence
# we can use the iter() function to create an iterator object from a sequence
# we can use the reversed() function to iterate over a sequence in reverse order
# we can use the sorted() function to sort a sequence
#  in ascending or descending order


